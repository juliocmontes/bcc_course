// Program to print out four triangles

#include <iostream>

using namespace std;

void triangle()
{
    cout << "  *\n ***\n*****\n";
}

int main()
{
    cout << "Wait for it folks..." << endl;
    triangle();
    cout << "And again:..." << endl;
    triangle();
    cout << "And again and again:..." << endl;
    triangle();
    triangle();
    cout << "That's all folks!" << endl;
    return 0;
}
